    public void EditUserDataInDb()
    {
        String srch=jTextField1.getText();
        String fname=FullNameTxt.getText();
        String Addres=AddressTxt.getText();
        String contac=contactTxt.getText();
        String Ag=AgeTxt.getText();
        String uname=Username1.getText();
        String pward=PasswordTxt1.getText();
        String gender = null;
             if(maleRBtn1.isSelected()){
                      gender = "Male";
         
            }else if (femaleRBtn1.isSelected()){
                      gender = "Female";
                    }
        
        try {
           pst=con.prepareStatement("UPDATE userdetails SET Fullname=?,Address=?,phoneNo=?,age=?,Gender=?,username=?,password=? WHERE userid =?");
            
            pst.setString(1, fname);
            pst.setString(2, Addres);
            pst.setString(3, contac);
            pst.setString(4, Ag);
            pst.setString(5, gender);
            pst.setString(6, uname);
            pst.setString(7, pward);
            pst.setString(8, srch);
            
            int k=pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(null,"Record Edited sucessfully!");
                
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Edit process failed!!");
            }
            
                FullNameTxt.setText("");
                AddressTxt.setText("");
                contactTxt.setText("");
                AgeTxt.setText("");
                Username1.setText("");
                PasswordTxt1.setText("");
                maleRBtn1.setSelected(true);
                jTextField1.setText(" ");
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

