    public void GetDataFromDB()
    {
        String srch=jTextField1.getText();
        
        try {
            pst=con.prepareStatement("SELECT * FROM userdetails WHERE userid ='"+srch+"'" );
            rs=pst.executeQuery();
            
            if(rs.next())
            {
            FullNameTxt.setText(rs.getString("Fullname"));
            AddressTxt.setText(rs.getString("Address"));
            contactTxt.setText(rs.getString("phoneNo"));
            AgeTxt.setText(rs.getString("age"));
            Username1.setText(rs.getString("username"));
            PasswordTxt1.setText(rs.getString("password"));
/////////////////////////////////////////////////////////////////////////////////////////// 
 /////// To set the valus of the radio buttons according to the database values////////////
            String gender=(rs.getString("Gender"));
            if(gender.equals("Male")){
                
                    maleRBtn1.setSelected(true);
                    
         
            }else{
                    femaleRBtn1.setSelected(true);
                
            }
            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    
    }
