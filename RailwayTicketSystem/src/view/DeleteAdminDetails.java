    public void LoadDataToTextfields()
    {
        String srch=SearchField2.getText();
        
       try {
            pst=con.prepareStatement("SELECT * FROM userdetails WHERE userid ='"+srch+"'" );
            rs=pst.executeQuery();
            
            if(rs.next())
            {
            FullNameTxt1.setText(rs.getString("Fullname"));
            AddressTxt1.setText(rs.getString("Address"));
            contactTxt1.setText(rs.getString("phoneNo"));
            AgeTxt1.setText(rs.getString("age"));
            Username2.setText(rs.getString("username"));
            PasswordTxt2.setText(rs.getString("password"));
/////////////////////////////////////////////////////////////////////////////////////////// 
 /////// To set the valus of the radio buttons according to the database values////////////
            String gender=(rs.getString("Gender"));
            if(gender.equals("Male")){
                
                    maleRBtn2.setSelected(true);
                    
         
            }else{
                    femaleRBtn2.setSelected(true);
                
            }
            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    
    }
