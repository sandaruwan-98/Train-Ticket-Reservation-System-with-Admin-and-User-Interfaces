    public void DeleteUserDataFromDB()
    {
        String tno=SearchField2.getText();
        
        try {
            pst=con.prepareStatement("DELETE FROM userdetails WHERE userid=?");
            pst.setString(1, tno);
            
            int k=pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(null,"Record Deleted!!");
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Deletion failed!!");
            }
            FullNameTxt1.setText("");
            AddressTxt1.setText("");
            contactTxt1.setText("");
            AgeTxt1.setText("");
            Username2.setText("");
            SearchField2.setText(" ");
            PasswordTxt2.setText(" ");
            maleRBtn2.setSelected(true);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

