    public void LoadDataToTextfields()
    {
        String srch=SearchField.getText();
        
        try {
            pst=con.prepareStatement("SELECT * FROM ttraindetails WHERE tno='"+srch+"'" );
            rs=pst.executeQuery();
            
            if(rs.next())
            {
            Txt1.setText(rs.getString("tno"));
            Txt2.setText(rs.getString("tname"));
            Txt3.setText(rs.getString("startplace"));
            Txt4.setText(rs.getString("destinationplace"));
            Txt5.setText(rs.getString("price"));
            }
            else{
                Txt1.setText("");
                Txt2.setText("");
                Txt3.setText("");
                Txt4.setText("");
                Txt5.setText("");
            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    
    }
