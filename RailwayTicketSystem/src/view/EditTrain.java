    public void EditDataToSql()
    {
        String tno=Txt1.getText();
        String trainname=Txt2.getText();
        String splace=Txt3.getText();
        String dplace=Txt4.getText();
        String price=Txt5.getText();
        
        try {
           pst=con.prepareStatement("UPDATE ttraindetails SET tname=?,startplace=?,destinationplace=?,price=? WHERE tno=?");
            
            pst.setString(1, trainname);
            pst.setString(2, splace);
            pst.setString(3, dplace);
            pst.setString(4, price);
            pst.setString(5, tno);
            
            int k=pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(null,"Record Edited sucessfully!");
                
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Edit process failed!!");
            }
            
                Txt1.setText("");
                Txt2.setText("");
                Txt3.setText("");
                Txt4.setText("");
                Txt5.setText("");
                SearchField.setText(" ");
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
