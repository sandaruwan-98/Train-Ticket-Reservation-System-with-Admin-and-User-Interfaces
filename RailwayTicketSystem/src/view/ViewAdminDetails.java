    // TO LOAD TRAIN DETAILS INTO THE J-TABLE
    public void UserDetails()
    {
        try {
            pst=con.prepareStatement("SELECT * FROM userdetails");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd=rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)jTable2.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
             Vector v=new Vector(); 
             for(int i =0;i<=c;i++){
                 v.add(rs.getString("userid"));
                 v.add(rs.getString("Fullname"));
                 v.add(rs.getString("Address"));
                 v.add(rs.getString("phoneNo"));
                 v.add(rs.getString("age"));
                 v.add(rs.getString("Gender"));
                 v.add(rs.getString("username"));
                 v.add(rs.getString("password"));
             }
                d.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

